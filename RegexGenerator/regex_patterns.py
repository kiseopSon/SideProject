def get_regex_pattern(pattern_name: str, language: str) -> str:
    """패턴 이름과 언어에 맞는 정규식을 반환"""
    if pattern_name in PATTERNS and language in PATTERNS[pattern_name]:
        base_pattern = PATTERNS[pattern_name][language]
        
        # 언어별 이스케이프 처리
        if language == "Java":
            # Java 문자열에서는 백슬래시를 이중으로 이스케이프해야 함
            return base_pattern.replace("\\", "\\\\")
        elif language == "PostgreSQL":
            # PostgreSQL에서는 백슬래시를 이중으로 이스케이프해야 함
            return base_pattern.replace("\\", "\\\\")
        elif language == "Oracle":
            # Oracle은 PostgreSQL과 유사하지만 일부 차이 가능
            return base_pattern.replace("\\", "\\\\")
        elif language == "MySQL":
            # MySQL은 PostgreSQL과 유사
            return base_pattern.replace("\\", "\\\\")
        elif language == "Django":
            # Django는 Python과 동일 (raw string 사용)
            return base_pattern
        else:
            return base_pattern
    return ""


def get_pattern_names() -> list:
    """사용 가능한 모든 패턴 이름 목록 반환"""
    return list(PATTERNS.keys())


def get_languages() -> list:
    """지원하는 모든 언어 목록 반환"""
    return ["Java", "Python", "JavaScript", "PostgreSQL", "Oracle", "MySQL", "Django"]
